#!/usr/bin/env python3
"""Regenerate bus/datasets/ Python modules from the canonical JSON files in lib/data/.

Usage:
    python bus/scripts/sync_datasets.py

This ensures the Fetch.ai uAgent Python code and the Next.js frontend
share the same verified, attributed data.
"""

import json
import os
import textwrap
from pathlib import Path
from datetime import datetime

ROOT = Path(__file__).resolve().parent.parent.parent  # project root
LIB_DATA = ROOT / "lib" / "data"
BUS_DATASETS = ROOT / "bus" / "datasets"


def sync_aeds() -> int:
    """Read aeds-ucla-ehs.json + aeds-osm.json and write bus/datasets/ucla_aeds.py."""
    ucla_path = LIB_DATA / "aeds-ucla-ehs.json"
    osm_path = LIB_DATA / "aeds-osm.json"

    with open(ucla_path) as f:
        ucla_data = json.load(f)
    with open(osm_path) as f:
        osm_data = json.load(f)

    aeds = ucla_data.get("aeds", [])
    osm_aeds = osm_data.get("aeds", [])

    lines = [
        '"""UCLA campus AED registry.',
        "",
        f"Auto-generated by sync_datasets.py on {datetime.now().strftime('%Y-%m-%d %H:%M')}.",
        f"Source JSON: lib/data/aeds-ucla-ehs.json ({len(aeds)} entries),",
        f"            lib/data/aeds-osm.json ({len(osm_aeds)} entries).",
        "Data source: UCLA Environmental Health & Safety public AED list",
        "License: Public information",
        "Note: Building centroid coordinates used where exact AED location is not published.",
        '"""',
        "",
        "UCLA_AEDS = [",
    ]

    for aed in aeds:
        lines.append("    {")
        lines.append(f'        "name": {json.dumps(aed["name"])},')
        if "building" in aed:
            lines.append(f'        "building": {json.dumps(aed["building"])},')
        lines.append(f'        "lat": {aed["lat"]},')
        lines.append(f'        "lon": {aed["lon"]},')
        lines.append(f'        "pads_available": {aed.get("padsAvailable", True)},')
        if "notes" in aed:
            lines.append(f'        "notes": {json.dumps(aed["notes"])},')
        lines.append("    },")

    lines.append("]")
    lines.append("")

    # Also include OSM AEDs as a separate list
    lines.append("OSM_AEDS = [")
    for aed in osm_aeds:
        lines.append("    {")
        lines.append(f'        "osm_id": {aed["id"]},')
        name = (
            aed.get("tags", {}).get("name")
            or aed.get("tags", {}).get("operator")
            or aed.get("tags", {}).get("defibrillator:location")
            or f'AED #{aed["id"]}'
        )
        lines.append(f'        "name": {json.dumps(name)},')
        lines.append(f'        "lat": {aed["lat"]},')
        lines.append(f'        "lon": {aed["lon"]},')
        lines.append("    },")
    lines.append("]")
    lines.append("")

    lines.append("# UCLA campus center for proximity filtering")
    lines.append("UCLA_CENTER = {")
    lines.append('    "lat": 34.0689,')
    lines.append('    "lon": -118.4452,')
    lines.append("}")
    lines.append("")
    lines.append("UCLA_CAMPUS_RADIUS_M = 1200  # Approximate campus radius in meters")
    lines.append("")

    out_path = BUS_DATASETS / "ucla_aeds.py"
    out_path.write_text("\n".join(lines))
    return len(aeds) + len(osm_aeds)


def sync_hospitals() -> int:
    """Read stemi-hospitals.json and write bus/datasets/la_stemi_hospitals.py."""
    hosp_path = LIB_DATA / "stemi-hospitals.json"

    with open(hosp_path) as f:
        data = json.load(f)

    hospitals = data.get("hospitals", [])

    lines = [
        '"""LA County STEMI Receiving Centers.',
        "",
        f"Auto-generated by sync_datasets.py on {datetime.now().strftime('%Y-%m-%d %H:%M')}.",
        f"Source JSON: lib/data/stemi-hospitals.json ({len(hospitals)} entries).",
        "Data source: California Department of Public Health STEMI Receiving Center list",
        "License: Public information",
        "Filtered to LA County facilities with 24/7 catheterization lab capability.",
        '"""',
        "",
        "LA_STEMI_HOSPITALS = [",
    ]

    for h in hospitals:
        lines.append("    {")
        lines.append(f'        "name": {json.dumps(h["name"])},')
        if "address" in h:
            lines.append(f'        "address": {json.dumps(h["address"])},')
        lines.append(f'        "lat": {h["lat"]},')
        lines.append(f'        "lon": {h["lon"]},')
        lines.append(f'        "cath_lab_24h": {h.get("cath_lab_24h", True)},')
        lines.append(f'        "ecmo_capable": {h.get("ecmo_capable", False)},')
        lines.append("    },")

    lines.append("]")
    lines.append("")

    out_path = BUS_DATASETS / "la_stemi_hospitals.py"
    out_path.write_text("\n".join(lines))
    return len(hospitals)


def main():
    print("Syncing datasets from lib/data/ JSON to bus/datasets/ Python modules...")
    print()

    aed_count = sync_aeds()
    print(f"  ucla_aeds.py       -> {aed_count} AED entries written")

    hosp_count = sync_hospitals()
    print(f"  la_stemi_hospitals.py -> {hosp_count} hospital entries written")

    print()
    print("Done. Python datasets are now in sync with the canonical JSON files.")


if __name__ == "__main__":
    main()
